package cn.com.oa.pojo;

public class WaiChuApp {
 
    private String WCDate;        // 外出时间
    private String WCBackDate;    // 返回时间
    private String WCDays;        // 外出天数
    private String WCReason;      // 外出原因
    
    public String getWCDate() {
        return WCDate;
    }
    public void setWCDate(String wCDate) {
        WCDate = wCDate;
    }
    public String getWCBackDate() {
        return WCBackDate;
    }
    public void setWCBackDate(String wCBackDate) {
        WCBackDate = wCBackDate;
    }   
    public String getWCReason() {
        return WCReason;
    }
    public void setWCReason(String wCReason) {
        WCReason = wCReason;
    }
    public String getWCDays() {
        return WCDays;
    }
    public void setWCDays(String wCDays) {
        WCDays = wCDays;
    }
    
}
